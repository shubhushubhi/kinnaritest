public class testQ15 {//15. Write a Java program to convert a given string into lowercase
    // Sample Input: THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG
    // Output: the quick brown fox jumps over the lazy dog.

    public static void main(String[] args) {
        String txt = "THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG";

        System.out.println(txt.toLowerCase()); // method for upper to lower or lower to upper case
    }
}